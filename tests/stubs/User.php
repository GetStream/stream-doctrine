<?php

namespace GetStream\Doctrine\Stubs;

class User
{
    /**
     * @var string $id
     */
    private $id;

    /**
     * @param string $id
     */
    public function __construct($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function id()
    {
        return $this->id;
    }
}
